package cooperativespace.core;

/**
 * This enum has an entry for each possible key-action that may be bound.
 */
public enum Action {

    // Movement actions
    ACCELERATE,
    REVERSE,
    ROTATE_RIGHT,
    ROTATE_LEFT,

    // Up to four abilities
    ABILITY_ONE,
    ABILITY_TWO,
    ABILITY_THREE,
    ABILITY_FOUR

}
